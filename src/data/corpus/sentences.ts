export type SentenceCategory = keyof SentenceCorpus;

export const getRandomSentences = (
    category: SentenceCategory,
    count: number
): string[] => {
    const sentences = SENTENCES[category];
    const result: string[] = [];
    const used = new Set<number>();

    while (result.length < count && result.length < sentences.length) {
        const index = Math.floor(Math.random() * sentences.length);
        if (!used.has(index)) {
            used.add(index);
            result.push(sentences[index]);
        }
    }

    return result;
};

